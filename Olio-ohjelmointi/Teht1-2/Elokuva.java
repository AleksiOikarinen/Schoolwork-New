public class Elokuva {
   private String elokuva;
   private int kesto;
   private String tuottaja;

   
   public String getElokuva() {
    return elokuva;
   }
   public void setElokuva(String elokuva) {
    this.elokuva = elokuva;
   }
   public int getKesto() {
    return kesto;
   }
   public void setKesto(int kesto) {
    this.kesto = kesto;
   }
   public String getTuottaja() {
    return tuottaja;
   }
   public void setTuottaja(String tuottaja) {
    this.tuottaja = tuottaja;
   }


}
